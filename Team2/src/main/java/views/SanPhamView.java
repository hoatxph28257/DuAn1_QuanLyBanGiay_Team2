    private void showData(List<QLSP> lists){
        model.setRowCount(0);
        
        for (QLSP list : lists) {
            model.addRow(new Object[]{list.getMaSP(), list.getTenSP()});
        }
    }
    private void fillData(int i) {
        QLSP sp = listSP.get(i);
        txtMa.setText(sp.getMaSP());
        txtTen.setText(sp.getTenSP());
    }

