    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        String ma = txtMa.getText();
        String ten = txtTen.getText();
        QLSP sp = new QLSP(ma, ten);
        JOptionPane.showMessageDialog(this, spService.save(sp));
        listSP.clear();
        listSP = spService.getAllSP();
        showData(listSP);
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        int row = tbSanPham.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Phải chọn dòng để sửa");
        } else {
            QLSP sp = listSP.get(row);
            sp.setMaSP(txtMa.getText());
            sp.setTenSP(txtTen.getText());
            JOptionPane.showMessageDialog(this, spService.update(sp));
            listSP.clear();
            listSP = spService.getAllSP();
            showData(listSP);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int row = tbSanPham.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Phải chọn dòng để xóa");
        } else {
            UUID id = listSP.get(row).getId();
            JOptionPane.showMessageDialog(this, spService.delete(id));
            listSP.remove(row);
            listSP.clear();
            listSP = spService.getAllSP();
            showData(listSP);
        }
    }//GEN-LAST:event_btnXoaActionPerformed

